var num1= Number(prompt("Enter 1st value:"));
var num2= Number(prompt("Enter 2nd value:"));
var oper= prompt("Enter operation to perform");
if (oper=="+")
document.write("<h1>" +"Result is "+(num1+num2)+ "</h1>");
else if(oper=="-")
document.write("<h1>" +"Result is "+(num1-num2)+ "</h1>");
else if (oper=="*")
document.write("<h1>" +"Result is "+(num1*num2)+ "</h1>");
else if(oper=="/")
document.write("<h1>" +"Result is "+(num1/num2) + "</h1>");
else if (oper=="%")
document.write("<h1>" +"Result is "+(num1%num2)+ "</h1>");


